void etimGetTimeTest(slong argc, schar *agrv[])
{
	EtimTm_t pTime;

	ETIM_GetTime(&pTime);
	printf("Current Time(from ETIM_GetTime):\n%04d-%02d-%02d %02d:%02d:%02d\n",
						pTime.tmYear, pTime.tmMon, pTime.tmMday,
						pTime.tmHour, pTime.tmMin, pTime.tmSec);
	printf("Current Time(from date command):\n");
	system("date");
}

