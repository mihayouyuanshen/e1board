slong ETEST_Start(ulong fact)
{
	ETEST_MSG_t msg;

	if (ESYS_START_FACT_FOR_TEST == fact)
	{
		msg.header.msgId = ETEST_MSGID_START;
		FRM_MsgSendAsync(ETEST_SPORT, sizeof(msg), &msg);
		ESYS_NotifyStartResult(ETEST_SPORT, MOD_ACTION_RESULT_SUCCESS);
	}
	else
	{
		/* 処理無し */
	}
}

