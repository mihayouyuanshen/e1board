void ETEST_MsgProc(FrmMsgHead_t const *msg)
{
	switch(msg->attr)
	{
	/* 非同期メッセージ */
	case FRM_MSG_ASYNC:
		switch (msg->msgId)
		{
		case ETEST_MSGID_START:
			printf("shell!!");
			ETEST_startTestShell();
			break;
		default:
			break;
		}
		break;
	/* イベントメッセージ */
	case FRM_MSG_EVENT:
		break;
	default:
		break;
	}
}

