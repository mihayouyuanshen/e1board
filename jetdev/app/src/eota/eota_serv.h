/* 開始メッセージ */
typedef struct _eota_start_t
{
	FrmMsgHead_t Header;						/* メッセージヘッダファイル		*/
	uchar		 fwStartMode;					/* 開始モード					*/
} EOTA_START_T;

/* 終了メッセージ */
typedef struct _eota_stop_t
{
	FrmMsgHead_t Header;			/* メッセージヘッダファイル		*/

} EOTA_STOP_T;

/* ユーザ更新時点 */
typedef struct _eota_use_upd_t
{
	FrmMsgHead_t Header;			/* メッセージヘッダファイル		*/

	uchar        UpdPoint;			/* 更新時点						*/
	
} EOTA_USE_UPD_T;

