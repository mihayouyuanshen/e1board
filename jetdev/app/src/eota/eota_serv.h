/* CMD64 受信 Dataメッセージ */
typedef struct _eota_cmd64_data_t
{
	FrmMsgHead_t Header;			/* メッセージヘッダファイル		*/
	uchar startOrStop;				/* 運転／停止 "0":停止, "1":運転 */
} EOTA_CMD64_DATA_T;

/* CMD65 受信 Dataメッセージ */
typedef struct _eota_cmd65_data_t
{
	FrmMsgHead_t Header;			/* メッセージヘッダファイル		*/
	char ret;						/* 0:OK 0以外：NG*/
} EOTA_CMD65_DATA_T;

