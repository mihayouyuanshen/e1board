slong EUPNP_Start(slong fact)
{
	EupnpStart_t msg;

	DEBUG_TRCIN();
	msg.Header.msgId = ELED_MSGID_START;
	msg.Fact = fact;
	FRM_MsgSendAsync(EUPNP_SPORT, sizeof(msg), &msg);
	DEBUG_TRCOUT();

	return 0;
}
