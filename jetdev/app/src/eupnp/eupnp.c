void EUPNP_MsgProc(FrmMsgHead_t const *msg)
{
	DEBUG_TRCIN();
	switch(msg->attr) {
	/* 非同期メッセージ */
	case FRM_MSG_ASYNC:
		switch(msg->msgId) {
		case EUPNP_MSGID_START:
			eupnp_ServStart();
			break;
		case EUPNP_MSGID_STOP:
			eupnp_ServStop();
			break;
		break;
		}
	}
	DEBUG_TRCOUT();
}
