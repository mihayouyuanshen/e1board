extern schar SRV_GetNotice(uint *maxNum, noticeList *tmp_dataResponse_P, uint *listNum)
{
	schar tmp_ret_SC = 0;
	uint tmp_i = 0;
	*listNum = 2u;
	
	if(NULL == tmp_dataResponse_P)
	{
		LOG_debug("Error(SRV_GetNotice): Param is NULL.\n" );
		return -1;
	}
	else
	{
		LOG_debug("HELLO WORLD!\n");

		for(tmp_i = 0; tmp_i < *listNum; tmp_i++)
		{
			tmp_dataResponse_P->noteInfo[tmp_i].writeDate = 2023;
			strcpy(tmp_dataResponse_P->noteInfo[tmp_i].err, "0");
			strcpy(tmp_dataResponse_P->noteInfo[tmp_i].errRtg, "1");

			tmp_dataResponse_P->fwInfo[tmp_i].writeDate = 2023;
			strcpy(tmp_dataResponse_P->fwInfo[tmp_i].err, "0");
			strcpy(tmp_dataResponse_P->fwInfo[tmp_i].fwUpdMsg, "1");
		}
	}
	return tmp_ret_SC;
}

